package Tipos;

public class Literal {

	public static void main(String[] args) {
		// Literais
		int numero;
		String cadeia;
		char caracter;
		boolean certoOuErrado;
		
		// Atribuição
		numero = 10;
		cadeia = "Java é muito simples";
		caracter = 'S';
		certoOuErrado = true;
		
		System.out.println("Número: " + numero);
		System.out.println("Valor da variável cadeia é: " + cadeia);
		System.out.println("Valor da variável caracter é: " + caracter);
		System.out.println("Valor da variável certoOuErrado: " + certoOuErrado);
		
	}

}
